	static function from_2to3_characters($two_char_word, $returnOnEmpty=FALSE) {
		if (array_key_exists($two_char_word, self::$codes)) {
			return self::$codes[$two_char_word];
		}
		return $returnOnEmpty;
	}
	
	static function from_3to2_characters($three_char_word, $returnOnEmpty=FALSE) {
		$inverted_conversion = array_flip(self::$codes);
		if (array_key_exists($three_char_word, $inverted_conversion)) {
			return $inverted_conversion[$three_char_word];
		}
		return $returnOnEmpty;
	}
	
	static function get_full_country_name($code, $returnOnEmpty=FALSE) {
		if (strlen($code)>2) {
			$code = self::from_2to3_characters($code);
		} 
		
		// Array Flip..
		$DUTCH_KEYS = array_flip(self::$DUTCH_NAMES);		
		if (array_key_exists($code, $DUTCH_KEYS))
			return $DUTCH_KEYS[ $code ];
		
		return $returnOnEmpty;
	}
